T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    num1, num2 = map(int, input().split())
    if num1 > 9 or num2 > 9:
        print('#{0} {1}'.format(test_case, -1))
    else:
        print('#{0} {1}'.format(test_case, num1*num2))
    # ///////////////////////////////////////////////////////////////////////////////////
